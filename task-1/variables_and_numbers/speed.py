# If you cross a 490 meterlong street in 7 minutes, calculate your
# speed in meters per second. Print the answer without any decimal
# point in it. Hint: Speed = Distance / Time

time = 7*60 #as we want in meter per second
distance = 490
speed = distance/time
print("Speed in metres per second  {}".format(int(speed)))
print("Speed in metres per second in decimal {}".format((speed)))